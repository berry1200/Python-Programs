a = float(input("enter 1st no : "))
b = float(input("enter 2nd no : "))
c = str(input("enter the operation to perform : "))
if c=='+':
    print("ans",a+b)
elif c=='-':
    print("ans",a-b)
elif c=='*':
     print("ans",a*b)
elif c=='/':
     print("ans",a/b)
elif c=='%':
      print("ans",a%b)
else:
     print("invalid")
     

     